def update_map_constants():
	MAP.CELL_SPACING = (WINDOW.WIDTH / MAP.COLS) * MAP.SPACING_TO_WIDTH_RATIO
	MAP.CELL_WIDTH = WINDOW.WIDTH / MAP.COLS - MAP.CELL_SPACING
	MAP.CELL_HEIGHT = WINDOW.HEIGHT / MAP.ROWS - MAP.CELL_SPACING

	# MAZE -> WORLD GENERATION
	MAP.MAP_SIZE = MAP.ROWS, MAP.COLS

	# ROOMS GENERATION -> ECS ARCHITECTURE
	MAP.GRID_ROWS = int(MAP.ROWS * (MAP.ROOM_HEIGHT - 1)) + 1
	MAP.GRID_COLS = int(MAP.COLS *(MAP.ROOM_WIDTH - 1)) + 1
	MAP.GRID_SIZE = MAP.GRID_ROWS, MAP.GRID_COLS

	# WORLD
	MAP.WORLD_WIDTH = MAP.GRID_ROWS * SPRITES.WIDTH
	MAP.WORLD_HEIGHT = MAP.GRID_ROWS * SPRITES.HEIGHT
	MAP.WORLD_SIZE = (MAP.WORLD_WIDTH, MAP.WORLD_HEIGHT)

	# GAME
	GAME.NUMBER_OF_GUARDS = MAP.ROWS * MAP.COLS // 2