import pygame
import time

pygame.init()

class WINDOW:
	WIDTH = 1000
	HEIGHT = 1000
	CENTER = WIDTH // 2, HEIGHT // 2
	TITLE = "Maze Explorer"
	SIZE = WIDTH, HEIGHT

	DEFAULT_SIZE = SIZE
	DEFAULT_CENTER = CENTER
	
	FPS = 60
	CLOCK = pygame.Clock()

class UPDATE:
	INPUTS_CHECKS_PER_SEC = 10
	RAYCASTS_PER_SEC = 5

class CAMERA:
	ZOOM = 2
	WIDTH = WINDOW.WIDTH / ZOOM
	HEIGHT = WINDOW.HEIGHT / ZOOM

	SIZE = WIDTH, HEIGHT

class SPRITES:
	WIDTH = 16
	HEIGHT = 16
	SIZE = (WIDTH, HEIGHT)

class MAP:
	SPACING_TO_WIDTH_RATIO = 0.05

	# MAZE GENERATION
	SEED = time.time()
	ROWS = 6
	COLS = 6

	# RENDERING
	CELL_SPACING = (WINDOW.WIDTH / COLS) * SPACING_TO_WIDTH_RATIO
	CELL_WIDTH = WINDOW.WIDTH / COLS - CELL_SPACING
	CELL_HEIGHT = WINDOW.HEIGHT / ROWS - CELL_SPACING

	# MAZE -> WORLD GENERATION
	MAP_SIZE = ROWS, COLS

	ROOM_WIDTH = 9
	ROOM_HEIGHT = 9
	ROOM_SIZE = (ROOM_WIDTH, ROOM_HEIGHT)

	# ROOMS GENERATION -> ECS ARCHITECTURE
	GRID_ROWS = int(ROWS * (ROOM_HEIGHT - 1)) + 1
	GRID_COLS = int(COLS * (ROOM_WIDTH - 1)) + 1
	GRID_SIZE = GRID_COLS, GRID_ROWS

	# WORLD
	WORLD_WIDTH = GRID_ROWS * SPRITES.WIDTH
	WORLD_HEIGHT = GRID_ROWS * SPRITES.HEIGHT
	WORLD_SIZE = (WORLD_WIDTH, WORLD_HEIGHT)


